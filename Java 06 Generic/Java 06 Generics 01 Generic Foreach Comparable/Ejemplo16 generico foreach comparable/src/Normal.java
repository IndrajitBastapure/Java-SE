
public class Normal {

	public void imprimirArregloEnteros(Integer [] arreglo){
		for (int i=0;i<arreglo.length;i++){
			System.out.println(arreglo[i]);
		}
			
	}
	
	public void imprimirArregloDouble(Double [] arreglo){
		for (int i=0;i<arreglo.length;i++)
			System.out.println(arreglo[i]);
	}

	public void imprimirArregloChar(Character [] arreglo){
		for (int i=0;i<arreglo.length;i++)
			System.out.println(arreglo[i]);
	}

}
